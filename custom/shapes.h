    static float* sphereVertices(int sectorCount, int stackCount, float radius, int& outCount)
    {
        std::vector<float> temp;

        float x, y, z, xy;
        float nx, ny, nz, lengthInv = 1.0f / radius;
        float s, t;

        float sectorStep = 2 * glm::pi<float>() / sectorCount;
        float stackStep = glm::pi<float>() / stackCount;
        float sectorAngle, stackAngle;

        for (int i = 0; i <= stackCount; ++i)
        {
            stackAngle = glm::pi<float>() / 2 - i * stackStep;
            xy = radius * cosf(stackAngle);
            z = radius * sinf(stackAngle);

            for (int j = 0; j <= sectorCount; ++j)
            {
                sectorAngle = j * sectorStep;

                x = xy * cosf(sectorAngle);
                y = xy * sinf(sectorAngle);

                temp.push_back(x);
                temp.push_back(y);
                temp.push_back(z);

                nx = x * lengthInv;
                ny = y * lengthInv;
                nz = z * lengthInv;
                temp.push_back(nx);
                temp.push_back(ny);
                temp.push_back(nz);

                s = (float)j / sectorCount;
                t = (float)i / stackCount;
                temp.push_back(s);
                temp.push_back(t);
            }
        }

        outCount = temp.size();
        float* arr = new float[outCount];
        std::copy(temp.begin(), temp.end(), arr);
        return arr;
    }
    // ------------------------------------------------------------------------
    static unsigned int* sphereIndices(int sectorCount, int stackCount, int& outCount)
    {
        std::vector<unsigned int> temp;

        for (int i = 0; i < stackCount; ++i)
        {
            int k1 = i * (sectorCount + 1);
            int k2 = k1 + sectorCount + 1;

            for (int j = 0; j < sectorCount; ++j, ++k1, ++k2)
            {
                if (i != 0)
                {
                    temp.push_back(k1);
                    temp.push_back(k2);
                    temp.push_back(k1 + 1);
                }

                if (i != (stackCount - 1))
                {
                    temp.push_back(k1 + 1);
                    temp.push_back(k2);
                    temp.push_back(k2 + 1);
                }
            }
        }

        outCount = temp.size();
        unsigned int* arr = new unsigned int[outCount];
        std::copy(temp.begin(), temp.end(), arr);
        return arr;
    }
